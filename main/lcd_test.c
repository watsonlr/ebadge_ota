void lcd_cmd(uint8_t cmd) {
    gpio_set_level(PIN_DC, 0);
    spi_transaction_t t = {.length=8, .tx_buffer=&cmd};
    spi_device_polling_transmit(spi, &t);
}

void lcd_data(const uint8_t *data, int len) {
    if (len == 0) return;
    gpio_set_level(PIN_DC, 1);
    spi_transaction_t t = {.length=len*8, .tx_buffer=data};
    spi_device_polling_transmit(spi, &t);
}

void lcd_init() {
    gpio_set_level(PIN_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(100));
    gpio_set_level(PIN_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(100));
    
    lcd_cmd(CMD_SWRESET);
    vTaskDelay(pdMS_TO_TICKS(150));
    lcd_cmd(CMD_SLPOUT);
    vTaskDelay(pdMS_TO_TICKS(500));
    lcd_cmd(CMD_COLMOD);
    uint8_t d1 = 0x55;
    lcd_data(&d1, 1);
    lcd_cmd(CMD_MADCTL);
    uint8_t d2 = 0xA8;  // 90 degrees clockwise: MY=1, MV=1, BGR=1
    lcd_data(&d2, 1);
    lcd_cmd(CMD_DISPON);
    vTaskDelay(pdMS_TO_TICKS(100));
}

void lcd_set_window(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
    uint8_t data[4];
    lcd_cmd(CMD_CASET);
    data[0] = x0>>8; data[1] = x0&0xFF; data[2] = x1>>8; data[3] = x1&0xFF;
    lcd_data(data, 4);
    lcd_cmd(CMD_PASET);
    data[0] = y0>>8; data[1] = y0&0xFF; data[2] = y1>>8; data[3] = y1&0xFF;
    lcd_data(data, 4);
    lcd_cmd(CMD_RAMWR);
}

void lcd_fill(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color) {
    lcd_set_window(x, y, x+w-1, y+h-1);
    uint8_t c[2] = {color>>8, color&0xFF};
    for (int i=0; i<w*h; i++) lcd_data(c, 2);
}

void lcd_char(uint16_t x, uint16_t y, char c, uint16_t fg, uint16_t bg) {
    int idx = (c >= ' ' && c <= 'Z') ? c-' ' : 0;
    // 12x16 font scaled 2x = 24x32 pixels
    for (int col=0; col<12; col++) {
        uint16_t column_data = serif_font[idx][col];
        for (int row=0; row<16; row++) {
            uint16_t pixel_color = (column_data & (1<<row)) ? fg : bg;
            // Draw 2x2 block for each font pixel
            lcd_fill(x+col*2, y+row*2, 2, 2, pixel_color);
        }
    }
}

void lcd_str(uint16_t x, uint16_t y, const char *s, uint16_t fg, uint16_t bg) {
    int cx = x;
    while (*s) {
        lcd_char(cx, y, *s++, fg, bg);
        cx += 26;  // 24 pixels + 2 pixel spacing
    }
}

void app_main() {
    ESP_LOGI(TAG, "Starting...");
    
    gpio_config_t io = {
        .pin_bit_mask = (1ULL<<PIN_DC) | (1ULL<<PIN_RST),
        .mode = GPIO_MODE_OUTPUT,
    };
    gpio_config(&io);
    
    spi_bus_config_t bus = {
        .mosi_io_num = PIN_MOSI,
        .sclk_io_num = PIN_CLK,
        .miso_io_num = -1,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = 4096,
    };
    spi_bus_initialize(SPI2_HOST, &bus, SPI_DMA_CH_AUTO);
    
    spi_device_interface_config_t dev = {
        .clock_speed_hz = 20*1000*1000,
        .mode = 0,
        .spics_io_num = PIN_CS,
        .queue_size = 7,
    };
    spi_bus_add_device(SPI2_HOST, &dev, &spi);
    
    lcd_init();
    ESP_LOGI(TAG, "LCD initialized");
    
    // Colors: RGB565
    uint16_t BLACK=0x0000, WHITE=0xFFFF, RED=0xF800, GREEN=0x07E0, BLUE=0x001F;
    
    // After 90 degree rotation: width=320, height=240
    lcd_fill(0, 0, 320, 240, BLACK);
    
    const char *text = "HELLO WORLD";
    int tw = strlen(text) * 26;  // 26 pixels per character (24 + 2 spacing)
    int x = (320 - tw) / 2;
    int y = (240 - 32) / 2;  // Center vertically (32 = 16*2 char height)
    
    lcd_str(x, y, text, WHITE, BLACK);
    ESP_LOGI(TAG, "Text displayed");
    
    uint16_t colors[] = {BLACK, RED, GREEN, BLUE};
    uint16_t tcolors[] = {WHITE, WHITE, BLACK, WHITE};
    
    int i = 0;
    while(1) {
        vTaskDelay(pdMS_TO_TICKS(3000));
        i = (i + 1) % 4;
        lcd_fill(0, 0, 320, 240, colors[i]);  // Rotated dimensions
        lcd_str(x, y, text, tcolors[i], colors[i]);
    }
}
